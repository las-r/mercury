# memory class
class Memory:
    def __init__(self):
        self.ram = np.zeros(65536, dtype=np.uint8)
        self.ram[:80] = FONTSET
        
    def __getitem__(self, index):
        return self.ram[index]
    
    def __setitem__(self, index, value):
        self.ram[index] = value